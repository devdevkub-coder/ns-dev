with payment_groups as (
  select
    coalesce(p.voucher_id, p.doc_no) as voucher_key,
    (array_agg(p.id order by p.created_at nulls first, p.id))[1] as payment_id,
    (array_agg(p.doc_no order by p.created_at nulls first, p.id))[1] as payment_doc_no,
    (array_agg(p.created_by order by p.created_at nulls first, p.id))[1] as created_by,
    (array_agg(p.updated_by order by p.updated_at desc nulls last, p.id desc))[1] as updated_by,
    min(coalesce(p.created_at, now())) as created_at,
    max(coalesce(p.updated_at, p.created_at, now())) as updated_at,
    sum(coalesce(p.amount, 0)) as amount_snapshot,
    sum(coalesce(p.net_amount, 0)) as net_amount_snapshot,
    bool_or(coalesce(p.status, 'active') = 'cancelled') as has_cancelled,
    (array_agg(p.supplier_id order by p.created_at nulls first, p.id))[1] as supplier_id,
    (array_agg(p.account_id order by p.created_at nulls first, p.id))[1] as account_id
  from public.payments p
  group by coalesce(p.voucher_id, p.doc_no)
),
payment_events as (
  select
    pg.*,
    'posted'::text as action,
    null::text as from_status,
    'active'::text as to_status,
    null::text as note,
    jsonb_build_object('source', 'migration_backfill') as meta,
    pg.created_at as event_created_at,
    pg.created_by as event_created_by,
    1 as sort_order
  from payment_groups pg

  union all

  select
    pg.*,
    'cancelled'::text as action,
    'active'::text as from_status,
    'cancelled'::text as to_status,
    null::text as note,
    jsonb_build_object('source', 'migration_backfill') as meta,
    pg.updated_at as event_created_at,
    pg.updated_by as event_created_by,
    2 as sort_order
  from payment_groups pg
  where pg.has_cancelled
),
numbered_payment_events as (
  select
    payment_events.*,
    s.code as supplier_code_snapshot,
    a.code as account_code_snapshot,
    row_number() over (
      partition by voucher_key
      order by event_created_at, sort_order
    ) as seq
  from payment_events
  left join public.suppliers s on s.id = payment_events.supplier_id
  left join public.accounts a on a.id = payment_events.account_id
)
