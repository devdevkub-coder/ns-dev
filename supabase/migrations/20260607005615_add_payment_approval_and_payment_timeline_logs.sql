select
  'PMTALLOC-' || p.doc_no || '-' || p.id::text as allocation_key,
  p.doc_no as payment_doc_no,
  coalesce(p.voucher_id, p.doc_no) as payment_voucher_id,
  coalesce(pa.doc_no, 'PMA-' || pa.id::text) as payment_approval_doc_no,
  pa.source_type,
  pa.source_doc_no_snapshot,
  coalesce(p.amount, 0) + coalesce(p.withholding_tax, 0) + coalesce(p.discount, 0) as allocated_amount,
  case
    when coalesce(p.status, 'active') = 'cancelled' then 'reversed'
    else 'active'
  end as status,
  coalesce(p.created_at, now()) as created_at,
  p.created_by,
  p.updated_at,
  p.updated_by,
  p.id as payment_id,
  pa.id as payment_approval_id
from public.payments p
join public.payment_approvals pa on pa.id = p.payment_approval_id
where p.payment_approval_id is not null;

with payment_groups as (
  select
    coalesce(p.voucher_id, p.doc_no) as voucher_key,
    (array_agg(p.id order by p.created_at nulls first, p.id))[1] as payment_id,
    (array_agg(p.doc_no order by p.created_at nulls first, p.id))[1] as payment_doc_no,
    bool_or(coalesce(p.status, 'active') = 'cancelled') as has_cancelled
  from public.payments p
  group by coalesce(p.voucher_id, p.doc_no)
),
statement_splits as (
  select
    'PMTSPLIT-' || bs.doc_no as split_key,
    pg.payment_doc_no,
    pg.voucher_key as payment_voucher_id,
    a.code as account_code_snapshot,
    a.name as account_name_snapshot,
    coalesce(bs.amount_out, 0) as amount,
    case
      when pg.has_cancelled then 'reversed'
      else 'active'
    end as status,
    bs.doc_no as bank_statement_doc_no,
    coalesce(bs.created_at, now()) as created_at,
    bs.created_by,
    bs.updated_at,
    bs.updated_by,
    pg.payment_id,
    bs.account_id,
    bs.id as bank_statement_id
  from public.bank_statement bs
  join payment_groups pg on pg.voucher_key = bs.ref_id
  left join public.accounts a on a.id = bs.account_id
  where bs.ref_type = 'PMT'
),
fallback_splits as (
  select
    'PMTSPLIT-' || p.doc_no || '-' || coalesce(p.voucher_id, p.id::text) || '-' || coalesce(p.account_id::text, 'NA') || '-' || coalesce(p.status, 'active') as split_key,
    p.doc_no as payment_doc_no,
    coalesce(p.voucher_id, p.doc_no) as payment_voucher_id,
    a.code as account_code_snapshot,
    a.name as account_name_snapshot,
    sum(coalesce(p.net_amount, 0)) as amount,
    case
      when coalesce(p.status, 'active') = 'cancelled' then 'reversed'
      else 'active'
    end as status,
    null::text as bank_statement_doc_no,
    min(coalesce(p.created_at, now())) as created_at,
    (array_agg(p.created_by order by p.created_at nulls first, p.id))[1] as created_by,
    max(p.updated_at) as updated_at,
    (array_agg(p.updated_by order by p.updated_at desc nulls last, p.id desc))[1] as updated_by,
    min(p.id) as payment_id,
    p.account_id,
    null::bigint as bank_statement_id
  from public.payments p
  left join public.accounts a on a.id = p.account_id
  where not exists (
    select 1
    from public.bank_statement bs
    where bs.ref_type = 'PMT'
      and bs.ref_id = coalesce(p.voucher_id, p.doc_no)
  )
  group by
    p.doc_no,
    coalesce(p.voucher_id, p.doc_no),
    coalesce(p.voucher_id, p.id::text),
    p.account_id,
    a.code,
    a.name,
    coalesce(p.status, 'active')
)
