with approval_events as (
  select
    pa.id as payment_approval_id,
    pa.doc_no as payment_approval_doc_no,
    pa.source_type,
    pa.source_id,
    pa.source_doc_no_snapshot,
    pa.approved_amount as approved_amount_snapshot,
    null::text as payment_doc_no,
    null::bigint as payment_id,
    'approved'::text as action,
    'pending'::text as from_status,
    'approved'::text as to_status,
    null::text as note,
    jsonb_build_object('source', 'migration_backfill') as meta,
    coalesce(pa.approved_at, pa.updated_at, pa.created_at, now()) as created_at,
    pa.approved_by as created_by,
    1 as sort_order
  from public.payment_approvals pa
  where pa.doc_no is not null
    and (pa.approved_at is not null or pa.status in ('approved', 'paid', 'voided'))

  union all

  select
    pa.id as payment_approval_id,
    pa.doc_no as payment_approval_doc_no,
    pa.source_type,
    pa.source_id,
    pa.source_doc_no_snapshot,
    pa.approved_amount as approved_amount_snapshot,
    p.doc_no as payment_doc_no,
    p.id as payment_id,
    'paid'::text as action,
    'approved'::text as from_status,
    'paid'::text as to_status,
    null::text as note,
    jsonb_build_object('source', 'migration_backfill', 'voucherId', p.voucher_id) as meta,
    coalesce(pa.paid_at, p.created_at, pa.updated_at, now()) as created_at,
    coalesce(p.created_by, pa.approved_by) as created_by,
    2 as sort_order
  from public.payment_approvals pa
  left join lateral (
    select p2.id, p2.doc_no, p2.voucher_id, p2.created_at, p2.created_by
    from public.payments p2
    where p2.payment_approval_id = pa.id
    order by p2.created_at desc nulls last, p2.id desc
    limit 1
  ) p on true
  where pa.doc_no is not null
    and (pa.paid_at is not null or pa.status = 'paid')

  union all

  select
    pa.id as payment_approval_id,
    pa.doc_no as payment_approval_doc_no,
    pa.source_type,
    pa.source_id,
    pa.source_doc_no_snapshot,
    pa.approved_amount as approved_amount_snapshot,
    p.doc_no as payment_doc_no,
    p.id as payment_id,
    case
      when p.id is not null then 'reversed_by_payment_cancel'
      else 'voided_before_payment'
    end::text as action,
    case
      when p.id is not null then 'paid'
      else 'approved'
    end::text as from_status,
    'voided'::text as to_status,
    pa.void_reason as note,
    jsonb_build_object('source', 'migration_backfill', 'voucherId', p.voucher_id) as meta,
    coalesce(pa.voided_at, pa.updated_at, now()) as created_at,
    coalesce(pa.voided_by, p.updated_by, pa.approved_by) as created_by,
    3 as sort_order
  from public.payment_approvals pa
  left join lateral (
    select p2.id, p2.doc_no, p2.voucher_id, p2.updated_by
    from public.payments p2
    where p2.payment_approval_id = pa.id
    order by p2.updated_at desc nulls last, p2.created_at desc nulls last, p2.id desc
    limit 1
  ) p on true
  where pa.doc_no is not null
    and (pa.status = 'voided' or pa.voided_at is not null)
),
numbered_approval_events as (
  select
    approval_events.*,
    row_number() over (
      partition by payment_approval_id
      order by created_at, sort_order, payment_id nulls first
    ) as seq
  from approval_events
)
